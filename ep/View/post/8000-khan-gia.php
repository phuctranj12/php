			<p>Bên cạnh những giai điệu rap sôi động, khán giả còn được chìm đắm vào những thanh âm da diết, chữa lành của ban nhạc Chillies. Những bản tình ca làm nên tên tuổi của nhóm như <em>Mascara, Vùng ký ức, Giấc mơ khác, Nếu ngày mai không đến</em> sẽ được Chillies mang lên sân khấu với bản phối đầy cảm xúc.</p>
